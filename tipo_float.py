#Profundizando en el tipo float
flotante = 3.5999
print(f'Float: {flotante:.2f}')

#Flotante var:.numF

floti = float('234')
print(f'floti: {floti:.2f}')

#Notación exponencial, valores positivos o negativo
a = 3e5
b = 3e-5
print(f'a: {a:.2f}')
print(f'b: {b:.5f}')

#Cualquier cálculo que incluye a un float, se convierte en un float